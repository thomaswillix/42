#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	sign;
	int	n;

	n = 0;
	sign = 1;
	while (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			sign *= -1;
		*nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		n = (*nptr - '0') + n * 10;
		*nptr++;
	}
	return (n);
}
